    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
         String maCT = txtmaCT.getText().trim();
    String maHD = txtmaHD.getText().trim();
    String maDV = txtmaDV.getText().trim();
    String soLuongStr = txtSoLuong.getText().trim();
    String donGiaStr = txtDonGia.getText().trim();

    if (maCT.isEmpty() || maHD.isEmpty() || maDV.isEmpty() || soLuongStr.isEmpty() || donGiaStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin!");
        return;
    }

    try {
        int soLuong = Integer.parseInt(soLuongStr);
        double donGia = Double.parseDouble(donGiaStr);

        Chitiethoadon ct = new Chitiethoadon();
        ct.setMaCT(maCT);
        ct.setMaHD(maHD);
        ct.setMaDV(maDV);
        ct.setSoLuong(soLuong);
        ct.setDonGia(donGia);

        if (dao.insert(ct)) {
            JOptionPane.showMessageDialog(null, "Thêm thành công!");
            loadTable(); 
        } else {
            JOptionPane.showMessageDialog(null, "Mã chi tiết đã tồn tại hoặc lỗi khi thêm!");
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Số lượng và đơn giá phải là số!");
    }
    }//GEN-LAST:event_btnthemActionPerformed

