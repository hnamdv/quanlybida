    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
                                     
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Vui lòng chọn chi tiết cần xoá!");
        return;
    }

    String maCT = jTable1.getValueAt(row, 0).toString(); // Lấy mã chi tiết

    int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xoá chi tiết này?");
    if (confirm == JOptionPane.YES_OPTION) {
        if (dao.delete(maCT)) {
            JOptionPane.showMessageDialog(null, "Xoá thành công!");
            loadTable();
        } else {
            JOptionPane.showMessageDialog(null, "Xoá thất bại!");
        }
    }
    }//GEN-LAST:event_btnxoaActionPerformed

